package com.github.graworks.radar;

public class PlanePosition {
    private final float xPos;
    private final float yPos;

    public PlanePosition(float xPos, float yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    public float getxPos() {
        return xPos;
    }

    public float getyPos() {
        return yPos;
    }
}
